    private void registrarButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_registrarButtonActionPerformed
        String nombre = nombreText.getText();
        String correo = correoText.getText();
        String rut = rutText.getText();
        String fono = fonoText.getText();
        String usuario = usuarioText.getText();
        String contrasena = contrasenaText.getText();
        int id = Integer.parseInt(idText.getText());
        int edad = Integer.parseInt(edadText.getText());
        if(ControladorPersona.validacionRegistro(usuario, contrasena) == false && id == 12345){
            Administrador p;
            p = new Administrador(id,fono,rut,usuario,nombre,correo,contrasena,edad);
            ControladorPersona.adm.add(p);
            nombreText.setText("");
            correoText.setText("");
            rutText.setText("");
            fonoText.setText("");
            usuarioText.setText("");
            contrasenaText.setText("");
            idText.setText("");
            edadText.setText("");
            mensajeAlerta("Administrador Registrado","Registro");
            File f = new File("./Administradores.csv");
            try(FileWriter fw = new FileWriter(f)){
                for(Administrador u: ControladorPersona.adm)
                    fw.write(u.toCSV()+"\n");
            } catch(Exception e){
                mensajeAlerta("No se ha encontrado el docuento","Registro");
                }            
        }
        else{
            usuarioText.setText("");
            contrasenaText.setText("");
            idText.setText("");
            mensajeAlerta("El usuario, contrase√±a o id invalidos","Registro");            
        }        
    }//GEN-LAST:event_registrarButtonActionPerformed

    private void idTextKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_idTextKeyTyped
        char validar = evt.getKeyChar();
        if(Character.isLetter(validar) || validar == ' '){
            getToolkit().beep();
            evt.consume();
            mensajeAlerta("Caracter incorrecto","Registro");
        }
    }//GEN-LAST:event_idTextKeyTyped

