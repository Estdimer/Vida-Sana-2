    private void registrarButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_registrarButtonActionPerformed
        String nombre = nombreText.getText();
        String correo = correoText.getText();
        double altura = Double.parseDouble(alturaText.getText().replace(',', '.'));
        double peso = Double.parseDouble(pesoText.getText().replace(',', '.'));
        String usuario = usuarioText.getText();
        String contrasena = contrasenaText.getText();
        int edad = Integer.parseInt(edadText.getText());
        if(ControladorPersona.validacionRegistro(usuario, contrasena) == false){
        for(Usuario p: ControladorPersona.uss)
            if(perfil.getUsuario().equals(p.getUsuario()) && perfil.getContrasena().equals(p.getContrasena())){
                p.setNombre(nombre);
                p.setCorreo(correo);
                p.setUsuario(usuario);
                p.setEdad(edad);
                p.setContrasena(contrasena);
                p.setAltura(altura);
                p.setPeso(peso);
                p.setEstado(ControladorPersona.getEstado(peso, altura));
            }
            mensajeAlerta("Perfil editado. Puede volver a loggear para verificar los cambios","Registro");
            dispose();
        }
        else{
            usuarioText.setText("");
            contrasenaText.setText("");
            mensajeAlerta("El usuario o contrase√±a ya ocupados","Registro");            
        }
    }//GEN-LAST:event_registrarButtonActionPerformed

    private void alturaTextKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_alturaTextKeyTyped
        char validar = evt.getKeyChar();
        if(Character.isLetter(validar) || validar == ' '){
            getToolkit().beep();
            evt.consume();
            mensajeAlerta("Caracter incorrecto","Registro");
        }
    }//GEN-LAST:event_alturaTextKeyTyped

